const INSTANCE_LABELS_EN = { ICC: "Icecrown Citadel", RS: "Ruby Sanctum" };
const DIFFICULTY_LABELS_EN = { "10N": "10 Normal", "10H": "10 Heroic", "25N": "25 Normal", "25H": "25 Heroic" };

function getNameLanguage() {
  return localStorage.getItem("nameLanguage") || "uk";
}

function setNameLanguage(lang) {
  localStorage.setItem("nameLanguage", lang);
}

function translateBoss(name) {
  return getNameLanguage() === "uk" ? (BOSS_NAMES_UK[name] || name) : name;
}

function translateItem(name) {
  return getNameLanguage() === "uk" ? (ITEM_NAMES_UK[name] || name) : name;
}

// instanceLabelsUk/difficultyLabelsUk — існуючі INSTANCE_LABELS/DIFFICULTY_LABELS
// з raid-manager-shared.js (уже українські за замовчуванням).
function translateInstance(code, instanceLabelsUk) {
  return getNameLanguage() === "uk" ? (instanceLabelsUk[code] || code) : (INSTANCE_LABELS_EN[code] || code);
}

function translateDifficulty(code, difficultyLabelsUk) {
  return getNameLanguage() === "uk" ? (difficultyLabelsUk[code] || code) : (DIFFICULTY_LABELS_EN[code] || code);
}
